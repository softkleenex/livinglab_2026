#!/bin/bash

# ==============================================================
# MDGA Auto Deployment Pipeline (Cloudflare Pages)
# ==============================================================

echo "==============================================="
echo "🌐 1단계: 프론트엔드 프로덕션 빌드 시작..."
echo "==============================================="
cd frontend || exit 1
npm run build

if [ $? -ne 0 ]; then
    echo "❌ 프론트엔드 빌드 실패! 에러 로그를 확인하세요."
    exit 1
fi

echo "==============================================="
echo "☁️ 2단계: Cloudflare Pages 배포 시작..."
echo "==============================================="

# npx wrangler pages deploy [디렉토리] [--project-name=...]
npx wrangler pages deploy dist --project-name=livinglab-2026

if [ $? -eq 0 ]; then
    echo "==============================================="
    echo "🎉 프론트엔드 Cloudflare 배포가 성공적으로 완료되었습니다!"
    echo "==============================================="
else
    echo "==============================================="
    echo "🚨 배포 실패! Cloudflare 권한(wrangler login) 및 설정을 확인하세요."
    echo "==============================================="
    exit 1
fi
